package com.pgl.energenius.enums;

/**
 * The EnergyType enum represents the different types of energy
 * <p>
 * The four possible energy types are:
 * <ul>
 * <li>GAZ: Natural gas energy</li>
 * <li>ELEC: Electrical energy</li>
 * <li>WATER: Water energy</li>
 * <li>ELEC_PRODUCTION: Electrical energy production (e.g. solar panels)</li>
 * </ul>
 */
public enum EnergyType {
    GAZ,
    ELEC,
    WATER,
    ELEC_PRODUCTION
}
